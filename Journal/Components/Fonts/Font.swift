  case BLACK = "-Black"
  case BOLD = "-Bold"
  case LIGHT = "-Light"
  case MEDIUM = "-Medium"
  case REGULAR = "-Regular"
}

enum FontSize: CGFloat {
  case LARGE_TITLE = 31
  case TITLE1 = 25
  case TITLE2 = 19
  case TITLE3 = 17
  case BODY = 14
  case CALLOUT = 13
  case SUBHEAD = 12
  case CAPTION = 11
}
